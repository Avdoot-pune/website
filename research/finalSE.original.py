def compute_probability(row):
    score = 0
    # Use normalized features for scale-invariant scoring
    score += 0.4 * min(row["cycle_time_norm"], 1)
    score += 0.2 * min(row["comments_norm"], 1)
    score += 0.15 * row["has_discussion"]
    score += 0.10 * (row["text_risk"] / 2)
    # Relative risk signals
    score += 0.15 * min(row["cycle_vs_repo"], 2) / 2

    if row["anomaly_flag" ] == 1:
        score += 0.2

    return min(score, 1.0)

df["probability"] = df.apply(compute_probability, axis=1)

def compute_impact(row):
    impact = 0
    # Fix: Allow more headroom to avoid score saturation at the top end
    impact += 0.4 * min(row["cycle_time_norm"], 1.5)
    impact += 0.3 * (row["text_risk"] / 2)
    if row["anomaly_flag"] == 1:
        impact += 0.3
    return min(impact, 0.95)

df["impact"] = df.apply(compute_impact, axis=1)
df["final_risk_score"] = (0.65 * df["probability"] + 0.35 * df["impact"])

