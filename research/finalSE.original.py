def fetch_prs(owner, repo, pages=5):
    all_prs = []
    url = f"https://api.github.com/repos/{owner}/{repo}/pulls"

    for page in range(1, pages+1):
        params = {"state": "closed", "per_page": 100, "page": page}

        for _ in range(3):
            res = requests.get(url, params=params)

            if res.status_code == 200:
                data = res.json()
                for pr in data:
                    pr["repo"] = repo   # 🔥 tag repo
                all_prs.extend(data)
                print(f"Fetched {repo} page {page}")
                break
            elif res.status_code == 500:
                time.sleep(2)
            else:
                print(f"Error fetching {repo}:", res.status_code)
                break

    return all_prs

