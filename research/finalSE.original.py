def apply_qcut(x):
    try:
        return pd.qcut(x, q=[0, 0.5, 0.75, 0.9, 1.0],
                      labels=["LOW", "MEDIUM", "HIGH", "CRITICAL"])
    except ValueError:
        # Fallback if there aren't enough unique values for qcut
        return pd.cut(x, bins=4, labels=["LOW", "MEDIUM", "HIGH", "CRITICAL"])

df["final_risk_level"] = df.groupby("repo")["final_risk_score"].transform(apply_qcut)

